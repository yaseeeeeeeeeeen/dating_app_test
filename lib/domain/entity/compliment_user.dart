class ComplimentUser {
  final String id;
  final String name;
  final int age;
  final String avatar;
  final String message;
  final int expiresInHours;
  final bool isNew;

  ComplimentUser({
    required this.id,
    required this.name,
    required this.age,
    required this.avatar,
    required this.message,
    required this.expiresInHours,
    required this.isNew,
  });
}
